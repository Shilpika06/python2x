"""Create a program that takes two number as input and prints whether first number is greater than,
less than or equal to the second number"""

x=int(input("Enter First number : "))
y=int(input ("Enter Second number : "))
print("First number is greater than Second Number", x>y)
print("First number is less than Second Number",x<y)
print("First number is equal to Second Number", x==y)
